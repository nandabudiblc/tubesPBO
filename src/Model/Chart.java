package Model;

/**
 *
 * @author Nanda
 */
public class Chart {
    private int count;
    private String nama;

    public Chart(int count, String nama) {
        this.count = count;
        this.nama = nama;
    }

    public Chart() {
    }

    
    /**
     * @return the count
     */
    public int getCount() {
        return count;
    }

    /**
     * @param count the count to set
     */
    public void setCount(int count) {
        this.count = count;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }
}
